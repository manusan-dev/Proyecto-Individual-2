    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed

        String status, comentariosTecnicos, tecnico;
        
        status = cmb_status.getSelectedItem().toString();
        comentariosTecnicos = jTextPane_comentariosTecnico.getText();
        tecnico = user;
        
        try {
            
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "update equipos set status =?, comentarios_tecnico =?, revision_tecnica_por =? where id_equipo =?");
            pst.setString(1, status);
            pst.setString(2, comentariosTecnicos);
            pst.setString(3, tecnico);
            pst.setInt(4, IDequipo);
            
            pst.executeUpdate();
            cn.close();
            
            JOptionPane.showMessageDialog(null, "Actualización realizada con éxito");
            
            this.dispose();
            
            
        } catch (SQLException e) {
            System.err.println("Error al actualizar equipo " + e);
            JOptionPane.showMessageDialog(null, "Error al actualizar equipo. Contacte al Administrador.");
        }
    }//GEN-LAST:event_jButton_ActualizarActionPerformed

