    public InformacionEquipoTecnico() {
        initComponents();
        user = Login.user;
        IDequipo = GestionarEquipos.IDequipo_update;
        
        setResizable(false);
        setSize(670, 530);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon fondoPantalla = new ImageIcon("src/main/java/images/wallpaper.jpg");
        Icon icono = new ImageIcon(fondoPantalla.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(), jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();

        Toolkit miPantalla = Toolkit.getDefaultToolkit();
        Image miIcono = miPantalla.getImage("src/main/java/images/icono.png");
        setIconImage(miIcono);
        
        try{
             Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement( 
           "select * from equipos where id_equipo =?" );
            pst.setInt(1, IDequipo);
            ResultSet rs = pst.executeQuery();
            
             
            if (rs.next()) {
                
                cmb_tipoEquipo.setSelectedItem(rs.getString("tipo_equipo"));
                cmb_marca.setSelectedItem(rs.getString("marca"));
                cmb_status.setSelectedItem(rs.getString("status"));
                txt_Modelo.setText(rs.getString("modelo"));
                txt_numSerie.setText(rs.getString("num_serie"));
                txt_ultimaMod.setText(rs.getString("ultima_modificacion"));
                
                String dia = "", mes = "", ano = "";
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                ano = rs.getString("ano_ingreso");
                txt_Fecha.setText(dia + " del " + mes + " de " + ano);
                
                jTextPane_observaciones.setText(rs.getString("observaciones"));
                jTextPane_comentariosTecnico.setText(rs.getString("comentarios_tecnico"));
                jLabel_revisionTecnicaDe.setText("Comentarios y actualizacion del Técnico: " + rs.getString("revision_tecnica_por"));
               
            }
            
            setTitle("Equipo registrado con el ID " + IDequipo + " - Sesión de " + user);
            
            
            
        } catch (SQLException e) {
            System.err.println("Error al consultar la información del equipo " + e);
        }
        
        
        
    }

